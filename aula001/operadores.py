# Operadores Aritiméticos

print(5 + 3)    # soma, retorna 8
print(8 - 6)    # subtração, retorna 2
print(4 * 3)    # multiplicação, retorna 12
print(10 / 2)   # divisão, retorna 5
print(5 ** 2)   # potência, retorna 25
print(5 % 2)    # módulo (resto) da divisão, retorna 1
print(10//3)    # arredonda o resultado da divisão, retorna 3

# Operadores de Comparação

## Compara os termos e retorna um valor booleano (True, False)

print(5 > 3)    # maior, retorna True
print(5 >= 3)   # maior ou igual, retorna True
print(5 < 3)    # menor, retorna False
print(5 <= 3)    # menor ou igual, retorna False
print(5 == 3)    # igualdade, retorna False
print(5 != 3)    # diferente, retorna True

# Operadores Lógicos

## and, or e not

(True) and (True)   # retorna True se ambos forem verdadeiros

(True) or (False)   # retorna True se ao menos um for verdadeiro

not(True)           # retorna o inverso do resultado
